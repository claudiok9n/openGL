public class MapParams {
    private int x;
    private int y;

    public MapParams(){}

    public void setX(int _x){
        x = _x;
    }
    public int getX(){
        return x;
    }

    public void setY(int _y){
        y = _y;
    }
    public int getY(){
        return y;
    }

}
